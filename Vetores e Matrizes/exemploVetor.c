#include <stdio.h>

int main() {
  // Declarando um vetor de inteiros com tamanho 5
  int vetor[5];

  // Inicializando o vetor com valores
  vetor[0] = 1;
  vetor[1] = 3;
  vetor[2] = 5;
  vetor[3] = 7;
  vetor[4] = 9;

  // Imprimindo os valores do vetor
  printf("Os valores do vetor sao: %d %d %d %d %d\n", vetor[0], vetor[1], vetor[2], vetor[3], vetor[4]);

  return 0;
}

