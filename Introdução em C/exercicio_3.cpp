//3- Como será interpretada a expressão
//x+++y?
//a) x++ + y b) x + ++y
//
//Existe duas formas de interpretar esse expressão.
//
//Se a expressão for usada em uma instrução separada, 
//a interpretação será "x++ + y", ou seja, o valor de "x" 
//será incrementado em 1 depois que a soma de "x" e "y" for realizada.
//
//Se a expressão for usada em uma expressão maior, 
//a interpretação será "x + ++y", ou seja, o valor de "y"
// será incrementado em 1 antes que a soma de "x" e "y" seja realizada.
